#include<stdio.h>

union data{
	unsigned long dword;
	unsigned char byte[4];
};

int main()
{
	union data d1;
	int i;
	printf("data 공용체의 크기 : %d\n", sizeof(union data));
	d1.dword = 0x12345678;
	printf("d1.dword = %08x\n", d1.dword);
	for (i = 0; i < 4; i++)
		printf("d1.byte[%d] = %02x\n", i, d1.byte[i]);
	/*cpu가 인텔계열이면 리틀 엔디안 방식으로 최하위바이트부터(78 56 34 12) 나오고
	모토로라계열이면 빅 엔디안 방식으로 최상위바이트부터(12 34 56 78) 나온다.*/
	return 0;
}